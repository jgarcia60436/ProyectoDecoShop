const _kPrivateApiFunctionName = 'ffPrivateApiCall';

class ApiTipoCambioCall {
  static Future<ApiCallResponse> call() {
    return ApiManager.instance.makeApiCall(
      callName: 'apiTipoCambio',
      apiUrl: 'https://tipodecambio.paginasweb.cr/api',
      callType: ApiCallType.GET,
      headers: {},
      params: {},
      returnBody: true,
      encodeBodyUtf8: false,
      decodeUtf8: false,
      cache: false,
    );
  }

  static dynamic compra(dynamic response) => getJsonField(
        response,
        r'''$.compra''',
      );
  static dynamic venta(dynamic response) => getJsonField(
        response,
        r'''$.venta''',
      );
  static dynamic fecha(dynamic response) => getJsonField(
        response,
        r'''$.fecha''',
      );
}

class ApiPagingParams {
  int nextPageNumber = 0;
  int numItems = 0;
  dynamic lastResponse;

  ApiPagingParams({
    required this.nextPageNumber,
    required this.numItems,
    required this.lastResponse,
  });

  @override
  String toString() =>
      'PagingParams(nextPageNumber: $nextPageNumber, numItems: $numItems, lastResponse: $lastResponse,)';
}

String _serializeList(List? list) {
  list ??= <String>[];
  try {
    return json.encode(list);
  } catch (_) {
    return '[]';
  }
}

String _serializeJson(dynamic jsonVar) {
  jsonVar ??= {};
  try {
    return json.encode(jsonVar);
  } catch (_) {
    return '{}';
  }
}
